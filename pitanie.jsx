// ─── HELPERS ─────────────────────────────────────────────────────────────────
function getMealMacros(meal, who) {
  if (meal.snack) return SNACKS[meal.snack] ?? {kcal:0,p:0,f:0,c:0};
  const r = RECIPES[meal.name];
  if (!r) return {kcal:0,p:0,f:0,c:0};
  return sumM(r.ings, who);
}
function getDayTotal(meals, who) {
  return meals.reduce((acc, meal) => {
    const m = getMealMacros(meal, who);
    return {kcal:acc.kcal+m.kcal, p:acc.p+m.p, f:acc.f+m.f, c:acc.c+m.c};
  }, {kcal:0,p:0,f:0,c:0});
}

